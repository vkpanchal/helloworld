a= "hello world"
print(a[2])# print value at 2
print(a[2:5])# slicing the value between position 2 and 5(excluded)
print(a[-5:-1])# Negative slicing

print (len(a))# length of the string
print(a.strip()) # return hello world
print(a.lower()) # lower case
print(a.upper()) # Upper case
print(a.replace("h", "v")) # replace h by V